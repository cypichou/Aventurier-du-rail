package ProjetAR;


public class Chemins {
    
    String ext1;
    String ext2 ;
    String couleur;
    int taille;
    boolean statut;
    boolean tunnel;
    /**
     * @return String return the ext1
     */
    public String getext1() {
        return ext1;
    }
    /**
     * @param ext1 the ext1 to set
     */
    public void setext1(String ext1) {
        this.ext1 = ext1;
    }
    /**
     * @return String return the ext2
     */
    public String getext2() {
        return ext2;
    }
    /**
     * @param ext2 the ext2 to set
     */
    public void setext2(String ext2) {
        this.ext2 = ext2;
    }
    /**
     * @return String return the couleur
     */
    public String getCouleur() {
        return couleur;
    }
    /**
     * @param couleur the couleur to set
     */
    public void setCouleur(String couleur) {
        this.couleur = couleur;
    }
    /**
     * @return int return the taille
     */
    public int getTaille() {
        return taille;
    }
    /**
     * @param taille the taille to set
     */
    public void setTaille(int taille) {
        this.taille = taille;
    }
    /**
     * @return boolean return the statut
     */
    public boolean isStatut() {
        return statut;
    }
    /**
     * @param statut the statut to set
     */
    public void setStatut(boolean statut) {
        this.statut = statut;
    }
    /**
     * @return boolean return the tunnel
     */
    public boolean isTunnel() {
        return tunnel;
    }
    /**
     * @param tunnel the tunnel to set
     */
    public void setTunnel(boolean tunnel) {
        this.tunnel = tunnel;
    }

}
